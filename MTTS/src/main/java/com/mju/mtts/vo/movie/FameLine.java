package com.mju.mtts.vo.movie;

public class FameLine {
	private String fameLineSeq;
	private String fameLine;
	private String writer;
	private String recommend;
	private String createDate;
	private String pwd;
	
	public String getFameLineSeq() {
		return fameLineSeq;
	}
	public void setFameLineSeq(String fameLineSeq) {
		this.fameLineSeq = fameLineSeq;
	}
	public String getFameLine() {
		return fameLine;
	}
	public void setFameLine(String fameLine) {
		this.fameLine = fameLine;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getRecommend() {
		return recommend;
	}
	public void setRecommend(String recommend) {
		this.recommend = recommend;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
}
