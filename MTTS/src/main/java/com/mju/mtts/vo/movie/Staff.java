package com.mju.mtts.vo.movie;

public class Staff {
	private String movieSeq;
	private String staffSeq;
	private String name;
	private String birthDate;
	private String birthLegion;
	private String profile;
	private String role;
	
	public String getStaffSeq() {
		return staffSeq;
	}
	public void setStaffSeq(String staffSeq) {
		this.staffSeq = staffSeq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirthDate() {
		return birthDate;
	}
	public void setBirthDate(String birthDate) {
		this.birthDate = birthDate;
	}
	public String getBirthLegion() {
		return birthLegion;
	}
	public void setBirthLegion(String birthLegion) {
		this.birthLegion = birthLegion;
	}
	public String getProfile() {
		return profile;
	}
	public void setProfile(String profile) {
		this.profile = profile;
	}
	public String getMovieSeq() {
		return movieSeq;
	}
	public void setMovieSeq(String movieSeq) {
		this.movieSeq = movieSeq;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
}
